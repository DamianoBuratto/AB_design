def analyze_one(base_path, args):
    """Run full \u0394\u0394G analysis for one design directory.  Returns True on success."""
    print("=" * 70)
    print(f"FEP \u0394\u0394G Analysis  |  {base_path}")
    print(f"T = {args.temp} K  |  skip {args.skip*100:.0f}%  |  "
          f"{args.windows} windows  |  max {args.replicas} replicas")
    print("=" * 70)

    # ── Load data ──────────────────────────────────────────────────────────────
    print("\nLoading data...")
    u_b_f, u_b_r = load_state_data(base_path, 'bound',
                                    args.windows, args.replicas, args.skip, args.temp)
    u_u_f, u_u_r = load_state_data(base_path, 'unbound',
                                    args.windows, args.replicas, args.skip, args.temp)

    if u_b_f is None:
        print("ERROR: No BOUND forward data. Check directory structure.")
        return False
    if u_u_f is None:
        print("ERROR: No UNBOUND forward data. Check directory structure.")
        return False

    # ── MBAR ───────────────────────────────────────────────────────────────────
    print("\nFitting MBAR...")
    print("  [HREX-FEP: fwd and rev both compute \u0394G(HIS\u2192ARG); expect fwd \u2248 rev (SAME sign, same magnitude)]")
    print("  [convergence check: |\u0394G_fwd - \u0394G_rev| should be < 1-2 kJ/mol]")
    print("  BOUND state:")
    dG_b_f, err_b_f, dG_b_r, err_b_r, dG_b, err_b, mbar_b = calc_mbar(u_b_f, u_b_r, args.temp)

    print("  UNBOUND state:")
    dG_u_f, err_u_f, dG_u_r, err_u_r, dG_u, err_u, mbar_u = calc_mbar(u_u_f, u_u_r, args.temp)

    if dG_b is None or dG_u is None:
        print("\nERROR: MBAR calculation failed.")
        return False

    # ── \u0394\u0394G (three estimates) ─────────────────────────────────────────────────
    has_rev  = dG_b_r is not None and dG_u_r is not None
    ddG_f    = dG_b_f - dG_u_f
    err_f_v  = np.sqrt(err_b_f**2 + err_u_f**2)
    ddG      = dG_b   - dG_u
    err      = np.sqrt(err_b**2   + err_u**2)
    if has_rev:
        ddG_r   = dG_b_r - dG_u_r
        err_r_v = np.sqrt(err_b_r**2 + err_u_r**2)

    def _row(dGb, eb, dGu, eu, ddg, ed):
        return (f"  {dGb:+9.2f} \u00b1 {eb:.2f}    {dGu:+9.2f} \u00b1 {eu:.2f}    {ddg:+7.2f} \u00b1 {ed:.2f}")

    print("\n" + "=" * 70)
    if has_rev:
        print("FINAL RESULTS  (MBAR)")
    else:
        print("FINAL RESULTS  (MBAR \u2014 forward only)")
    print("=" * 70)
    _h1 = "\u0394G_bound (kJ/mol)"; _h2 = "\u0394G_unbound (kJ/mol)"; _h3 = "\u0394\u0394G_binding (kJ/mol)"
    print(f"  {'':11s}  {_h1:^18s}   {_h2:^18s}   {_h3:^18s}")
    print("-" * 70)
    print(f"  fwd-only :" + _row(dG_b_f, err_b_f, dG_u_f, err_u_f, ddG_f, err_f_v))
    if has_rev:
        print(f"  rev-only :" + _row(dG_b_r, err_b_r, dG_u_r, err_u_r, ddG_r, err_r_v))
        print(f"  combined :" + _row(dG_b, err_b, dG_u, err_u, ddG, err) + "  \u2190 final")
    print("=" * 70)

    if has_rev:
        conv = abs(ddG_f - ddG_r)
        conv_note = "OK" if conv <= 2.0 else "WARNING: consider more sampling"
        print(f"\n  Convergence |\u0394\u0394G_fwd \u2212 \u0394\u0394G_rev| = {conv:.2f} kJ/mol  [{conv_note}]")

    final_ddG = ddG
    if   final_ddG >  2.0:
        print(f"\n  \u0394\u0394G = {final_ddG:+.1f} kJ/mol  \u2192 Mutation WEAKENS binding")
    elif final_ddG < -2.0:
        print(f"\n  \u0394\u0394G = {final_ddG:+.1f} kJ/mol  \u2192 Mutation STRENGTHENS binding")
    else:
        print(f"\n  \u0394\u0394G \u2248 {final_ddG:+.1f} kJ/mol  \u2192 No significant effect (within \u00b12 kJ/mol)")

    # ── Save results ───────────────────────────────────────────────────────────
    out_file = os.path.join(base_path, 'ddG_results.txt')
    with open(out_file, 'w', encoding='utf-8') as f:
        f.write("FEP \u0394\u0394G_binding Analysis Results (MBAR)\n")
        f.write("=" * 70 + "\n\n")
        f.write(f"Directory:   {base_path}\n")
        f.write(f"Temperature: {args.temp} K\n")
        f.write(f"Skip:        {args.skip:.1%}\n")
        f.write(f"kBT:         {R_KJ * args.temp:.4f} kJ/mol\n\n")
        f.write(f"fwd-only : \u0394G_bound={dG_b_f:+.3f}\u00b1{err_b_f:.3f}  "
                f"\u0394G_unbound={dG_u_f:+.3f}\u00b1{err_u_f:.3f}  "
                f"\u0394\u0394G={ddG_f:+.3f}\u00b1{err_f_v:.3f} kJ/mol\n")
        if has_rev:
            f.write(f"rev-only : \u0394G_bound={dG_b_r:+.3f}\u00b1{err_b_r:.3f}  "
                    f"\u0394G_unbound={dG_u_r:+.3f}\u00b1{err_u_r:.3f}  "
                    f"\u0394\u0394G={ddG_r:+.3f}\u00b1{err_r_v:.3f} kJ/mol\n")
            f.write(f"combined : \u0394G_bound={dG_b:+.3f}\u00b1{err_b:.3f}  "
                    f"\u0394G_unbound={dG_u:+.3f}\u00b1{err_u:.3f}  "
                    f"\u0394\u0394G={ddG:+.3f}\u00b1{err:.3f} kJ/mol  \u2190 final\n")
            f.write(f"\nConvergence |\u0394\u0394G_fwd \u2212 \u0394\u0394G_rev| = {abs(ddG_f - ddG_r):.3f} kJ/mol\n")
        f.write(f"\n\u0394\u0394G_binding (final) = {ddG:+.3f} \u00b1 {err:.3f} kJ/mol"
                f"  ({ddG/4.184:+.3f} \u00b1 {err/4.184:.3f} kcal/mol)\n")
    print(f"\nResults saved: {out_file}")

    # ── Plots ─────────────────────────────────────────────────────────────────
    if not args.no_plots:
        print("\nGenerating plots...")
        plot_overlap_matrix(
            mbar_b, os.path.join(base_path, 'mbar_overlap_bound.png'),
            'MBAR Overlap Matrix \u2014 Bound')
        plot_overlap_matrix(
            mbar_u, os.path.join(base_path, 'mbar_overlap_unbound.png'),
            'MBAR Overlap Matrix \u2014 Unbound')
        plot_free_energy_profile(
            mbar_b, args.temp,
            os.path.join(base_path, 'free_energy_profile_bound.png'),
            'Free Energy Profile \u2014 Bound')
        plot_free_energy_profile(
            mbar_u, args.temp,
            os.path.join(base_path, 'free_energy_profile_unbound.png'),
            'Free Energy Profile \u2014 Unbound')
        plot_convergence(
            u_b_f, u_b_r,
            os.path.join(base_path, 'convergence_bound.png'),
            'Sampling \u2014 Bound')
        plot_convergence(
            u_u_f, u_u_r,
            os.path.join(base_path, 'convergence_unbound.png'),
            'Sampling \u2014 Unbound')
        print(f"  Plots saved to: {base_path}")

    print("\nDone.")
    return True


