#!/usr/bin/env python3
"""
FEP ΔΔG_binding Analysis - MBAR Method with Visualization

Directory structure (generated by setup_fep.sh + submit_fep.sh):

    <base_path>/
    ├── forward/
    │   ├── bound_R1/lambda{0..31}/PROD/prod.xvg
    │   ├── bound_R2/lambda{0..31}/PROD/prod.xvg
    │   ├── bound_R3/lambda{0..31}/PROD/prod.xvg
    │   ├── unbound_R1/lambda{0..31}/PROD/prod.xvg
    │   ├── unbound_R2/lambda{0..31}/PROD/prod.xvg
    │   └── unbound_R3/lambda{0..31}/PROD/prod.xvg
    └── reverse/          [optional; improves MBAR accuracy when available]
        ├── bound_R{1..N}/lambda{0..31}/PROD/prod.xvg
        └── unbound_R{1..N}/lambda{0..31}/PROD/prod.xvg

Forward vs Reverse in HREX-FEP (NOT non-equilibrium FEP):
  - HREX-FEP: each window runs at a FIXED lambda; lambda does NOT change during simulation.
  - Forward: 32 windows at lambda=0..1, starting structures taken from HIS-end conformation.
  - Reverse: same 32 windows at the SAME lambda=0..1, but starting structures taken from the
    ARG-end conformation (last frame of forward lambda31).
  - Both directions compute ΔG(HIS→ARG). MBAR result: fwd ≈ rev, SAME sign.
  - Combining fwd+rev samples improves sampling: fwd seeds HIS-like conformations (good low-λ),
    rev seeds ARG-like conformations (good high-λ). Together reduces systematic sampling gaps.
  - Convergence check: |ΔG_fwd - ΔG_rev| should be < 1-2 kJ/mol.
  [Contrast: BAR with non-equilibrium switching DOES give ΔG_rev = -ΔG_fwd; that is a
   different technique and does NOT apply here.]

Usage:
    python analyze_fep.py /path/to/outputs/design_name
    python analyze_fep.py /path/to/outputs/design_name --skip 0.2
    python analyze_fep.py /path/to/outputs/design_name --no-plots
"""

